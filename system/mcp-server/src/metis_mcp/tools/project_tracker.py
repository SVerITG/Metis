"""
project_tracker.py — Project activity scanning, category management,
CLAUDE.md generation, Claude Desktop registration.

New tools:
  scan_project_folder      — detect work done since last scan (git, files, todos)
  scan_folder_for_intent   — auto-detect project purpose from folder contents
  update_all_projects      — scan every registered project folder
  link_claude_desktop      — register a project in Claude Desktop's config
  get_project_categories   — list all categories in use
  set_project_category     — assign/rename a project's category
"""

import datetime
import json
import os
import re
import subprocess
from pathlib import Path

from mcp.types import TextContent

from metis_mcp.config import paths
from metis_mcp.db import connect
from metis_mcp.app_instance import app


# ── Helpers ───────────────────────────────────────────────────────────────────

def _ensure_columns(conn) -> None:
    existing = {row[1] for row in conn.execute("PRAGMA table_info(projects)")}
    for col, defn in [
        ("category",       "TEXT DEFAULT ''"),
        ("last_scanned",   "TEXT DEFAULT NULL"),
        ("scan_summary",   "TEXT DEFAULT ''"),
        ("claude_desktop_linked", "INTEGER DEFAULT 0"),
    ]:
        if col not in existing:
            conn.execute(f"ALTER TABLE projects ADD COLUMN {col} {defn}")


def _wsl_to_windows(path: str) -> str:
    """Convert /mnt/c/... to C:\\... for Windows tool calls."""
    m = re.match(r"^/mnt/([a-zA-Z])/(.*)", path)
    if m:
        return m.group(1).upper() + ":\\" + m.group(2).replace("/", "\\")
    return path


def _windows_to_wsl(path: str) -> str:
    """Convert C:\\... to /mnt/c/..."""
    m = re.match(r"^([A-Za-z]):[/\\](.*)", path)
    if m:
        return f"/mnt/{m.group(1).lower()}/{m.group(2).replace(chr(92), '/')}"
    return path


def _resolve_folder(external_path: str) -> Path | None:
    if not external_path:
        return None
    candidates = [Path(external_path), Path(_windows_to_wsl(external_path))]
    for p in candidates:
        if p.exists():
            return p
    return None


def _write_claude_md(folder: Path, project: dict, tasks: list[dict]) -> bool:
    """Write/refresh CLAUDE.md in the project folder."""
    open_tasks = [t for t in tasks if t.get("status") not in ("done", "cancelled")]
    done_tasks  = [t for t in tasks if t.get("status") == "done"]
    category = project.get("category") or project.get("domain") or "Research"
    lines = [
        f"# {project['title']}",
        "",
        f"**Category:** {category}  ",
        f"**Project ID:** `{project['project_id']}`  ",
        f"**Status:** {project.get('status', 'active')}",
        "",
    ]
    if project.get("description"):
        lines += ["## About this project", project["description"], ""]
    if project.get("next_step"):
        lines += ["## Current focus", project["next_step"], ""]
    if open_tasks:
        lines.append("## Open tasks")
        for t in open_tasks[:10]:
            lines.append(f"- [ ] {t['title']}")
        lines.append("")
    if done_tasks:
        lines.append(f"## Recently completed ({len(done_tasks)} tasks)")
        for t in done_tasks[:5]:
            lines.append(f"- [x] {t['title']}")
        lines.append("")
    lines += [
        "## Metis integration",
        "The Metis MCP server (`metis-rc`) tracks this project. Use these tools:",
        f"- `create_task(title=..., project_id=\"{project['project_id']}\")` — add a task",
        "- `get_tasks(project_id=...)` — see open tasks",
        "- `update_project_memory(project_id=..., what_was_done=..., next_steps=...)` — log a session",
        "- `scan_project_folder(project_id=...)` — refresh activity in Metis dashboard",
        "",
        "_Auto-generated by Metis. Re-run `scan_project_folder` to refresh._",
    ]
    target = folder / "CLAUDE.md"
    # GUARD: never clobber a hand-authored CLAUDE.md (the repo's operating guide, or a
    # user's own project file). Only write if the file is absent or was itself a Metis-
    # generated card. This prevents the root CLAUDE.md from being overwritten when a
    # project's folder happens to be the repo root (the metis-dashboard case).
    MARKER = "_Auto-generated by Metis."
    if target.exists():
        try:
            existing = target.read_text(encoding="utf-8", errors="replace")
        except OSError:
            existing = ""
        if MARKER not in existing:
            return False  # hand-authored — leave it alone
    # Extra belt: refuse if this folder is clearly the Metis repo root, not a project.
    if (folder / "system" / "mcp-server").is_dir() and (folder / "agents").is_dir():
        return False
    try:
        target.write_text("\n".join(lines), encoding="utf-8")
        return True
    except OSError:
        return False


